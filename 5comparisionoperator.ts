/*

Comparision operators are used to make a comparision b/w two or more values .

Developer can use comparision operator to perform comparision b/w two or more values.

it returns a boolean value i.e true or false according to given condition.

following operators are used for performing comparision b/w two or more values.

1-   <      (less than)
2-   >      (greater than)
3-   >=     (greater than or equals to)
4-   <=     (less than or equal to)
5-   !==    (not equal to)
6-   ==     (equal to) //checks only value.
7    ===    (identical to or strictly equals to type as well as in value)

*/

let myval1=34;
let myval2=23;
console.log(myval1 > myval2) ;   // true
console.log(myval1 < myval2) ;   // false
console.log(myval1 >= myval2) ;  // true
console.log(myval1 <= myval2) ;  // false
console.log(myval1 == myval2) ;  // false
console.log(myval1 !== myval2) ; //  true
console.log(myval1 === myval2) ; //  false
