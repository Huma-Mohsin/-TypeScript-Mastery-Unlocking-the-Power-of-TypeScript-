/*
Unary operators works only a single operand or variable. */

let x=45;
 console.log(++x); //preincrement 46

//it uses the current value of the variable in the expression, and then changes the value of the variable afterwards. This is called "post-increment" or "post-decrement".

 console.log(x++); //postincrement 46
 console.log(--x); //46
 console.log(x--); //46
 console.log(!x);  //false  

